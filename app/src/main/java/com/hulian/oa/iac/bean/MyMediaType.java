package com.hulian.oa.iac.bean;

public class MyMediaType {
    //ID
    private String id;
    //类型
    private String type;
    //路径
    private String path;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
